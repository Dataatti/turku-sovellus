import KerroKantasiCard from 'components/kerrokantasi/KerroKantasiCard';
import type { GetStaticProps } from 'next';
import Head from 'next/head';
import useTurkuEvents from 'hooks/useTurkuEvents';

const TurussaTapahtuu = ({ locale }: { locale: Lang }) => {
  const { isLoading, data } = useTurkuEvents();
  console.log(data, isLoading);

  return (
    <div>
      <Head>
        <title>Turussa tapahtuu</title>
        <meta name="description" content="Generated by create next app" />
      </Head>

      <main>
        {!isLoading &&
          data &&
          data.map((hearing) => (
            <KerroKantasiCard hearing={hearing} locale={locale} key={hearing.id} />
          ))}
      </main>
    </div>
  );
};

export const getStaticProps: GetStaticProps = async ({ locale }) => {
  return {
    props: {
      locale: locale || 'fi',
    },
  };
};

export default TurussaTapahtuu;
